		<h1 class="entry-title">Pedagogy of Programming&nbsp;Tools</h1>
		<div class="entry-meta">
			<span class="entry-date">January 25, 2020</span> 
					</div><!-- .entry-meta -->
          <br>
	</header><!-- .entry-header -->
      <div class="entry-content">
       <br>
      <br>
      <p> If you want to teach programming, you have several decisions you need to make. You need to choose:</p>
        <ul>
          <li>a programming language, such as Java, Python, Javascript, Ruby,</li>
          <li>a programming environment, which may be something like Notepad + command-line, or a full-blown IDE like Visual Studio,</li>
          <li>a context, such as making games, media computation, website creation, robotics, and</li>
          <li>a pedagogical approach, such as what you will teach, in what order, and using which activities.</li>
        </ul>
            <p>Not everyone thinks about that last item in terms of explicitly deciding on a pedagogical approach.  But as soon as you start making decisions such as: &#8220;what task do I start with?&#8221;, you are implicitly deciding.  Do you start with &#8220;What is a variable?&#8221; or &#8220;Here&#8217;s how to print &#8216;Hello World'&#8221; or &#8220;This is the syntax of a function call&#8221;?  Do you teach automated testing?  Do you start with a blank program or start by modifying an existing program?  You have always chosen a pedagogical approach, whether you realise it or not.</p>
            <p>What&#8217;s interesting about the four items above is that they <i>all</i> interact with each other.  The top three clearly so: you can&#8217;t write Java in IDLE, for example, and you may find your robot of choice doesn&#8217;t support Javascript.  But the tool and the language you choose will affect the available pedagogy and vice versa.  
            Programming tools are not pedagogy-neutral.  Your tool determines which programming-related activities are easy and which are hard, which in turn will affect how you use the tool to teach.</p>
            <p>Code tracing is a useful skill but doing it an environment with a debugger that shows variable values step-by-step makes it much easier than in Notepad+command-line.  Parsons problems (where you drag bits of pre-written code into order) are easier in Scratch than in a text editor.  BlueJ lets you call methods on objects via the context menu without writing any code, whereas an IDE like IntelliJ does not.  It&#8217;s useful to understand what pedagogies your tool supports or makes difficult when making a choice.</p>
            <p>In our latest Greenfoot Live video, my colleague Hamza and I sat down for half an hour to do some Greenfoot programming and talk about pedagogical strategies in Greenfoot: ways you can use it to teach, and what pedagogical approaches we have in mind when designing the tool.  I&#8217;m quite pleased with how it turned out, and I think it&#8217;s worth watching:</p>
            <div class="jetpack-video-wrapper"><span class="embed-youtube" style="text-align:center; display: block;"><iframe class='youtube-player' width='560' height='315' src='https://www.youtube.com/embed/RKNJ5I3voPg?version=3&#038;rel=1&#038;fs=1&#038;autohide=2&#038;showsearch=0&#038;showinfo=1&#038;iv_load_policy=1&#038;wmode=transparent' allowfullscreen='true' style='border:0;'></iframe></span></div>
            <p>Whether you agree with our particular pedagogical philosophies or not, next time you choose a programming language and tool, be aware of its impact on what teaching approaches and activities it can support well, and which activities it will make hard for you to engage in.</p>
